#include "libft.h"

char	*ft_strrchr(const char	*s, int	c)
{
	char	*ps;

	ps = NULL;
	while (*s)
	{
		if (*s == (char)c)
			ps = (char *)s;
		++s;
	}
	if (*s == (char)c)
		ps = (char *)s;
	return (ps);
}
